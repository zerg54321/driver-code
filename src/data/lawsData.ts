let cachedViolations: ViolationItem[] | null = null;

export function getViolationsList(): ViolationItem[] {
  if (cachedViolations) return cachedViolations;

  const lines = RAW_CSV.trim().split('\n');
  const result: ViolationItem[] = [];

  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    const parts = line.split(',');
    if (parts.length < 5) continue;

    const [
      id = '',
      code = '',
      behavior = '',
      behaviorLaw = '',
      punishLaw = '',
      remark = '',
      score = '',
      warning = '',
      fine = '',
      suspendMonth = '',
      detentionDay = '',
      revoke = '',
      forceMeasure = '',
      forceMeasureLaw = ''
    ] = parts;

    let category: ViolationItem['category'] = '道路交通';
    if (behavior.includes('行人') || behavior.includes('乘车')) {
      category = '行人和乘车人';
    } else if (behavior.includes('高速') || behavior.includes('快速路')) {
      category = '高速及快速路';
    } else if (behavior.includes('自行车') || behavior.includes('三轮') || behavior.includes('畜力') || behavior.includes('非机动车')) {
      category = '非机动车';
    } else if (behavior.includes('驾驶') || behavior.includes('客运') || behavior.includes('货运') || behavior.includes('车道') || behavior.includes('停车') || behavior.includes('灯光')) {
      category = '机动车通行';
    }

    result.push({
      id,
      code,
      behavior,
      behaviorLaw,
      punishLaw,
      remark,
      score,
      warning,
      fine,
      suspendMonth,
      detentionDay,
      revoke,
      forceMeasure,
      forceMeasureLaw,
      category
    });
  }

  cachedViolations = result;
  return result;
}

/**
 * 智能精准法条匹配
 * 彻底解决《法》、《条例》、《办法》子串重叠误判问题
 */
export function matchLawArticles(lawRef: string): { title: string; content: string }[] {
  if (!lawRef) return [];
  const matched: { title: string; content: string }[] = [];

  Object.keys(LAW_ARTICLES_DICT).forEach((dictKey) => {
    let isMatched = false;

    if (dictKey.startsWith('办法')) {
      // 《办法》前缀具有唯一性，直接判断
      if (lawRef.includes(dictKey)) {
        isMatched = true;
      }
    } else if (dictKey.startsWith('条例')) {
      // 《条例》前缀同样具有唯一性
      if (lawRef.includes(dictKey)) {
        isMatched = true;
      }
    } else if (dictKey.startsWith('法')) {
      // 《法》必须严防与《办法》或《条例》或其它文字中的“法”字重叠（如“办法第九十八条”）
      let pos = lawRef.indexOf(dictKey);
      while (pos !== -1) {
        // 检查前一个字符，如果前一个字符是“办”或“条”或“规”等，说明属于其它条例
        const prevChar = pos > 0 ? lawRef[pos - 1] : '';
        if (prevChar !== '办' && prevChar !== '条' && prevChar !== '规' && prevChar !== '办' && prevChar !== '施') {
          isMatched = true;
          break;
        }
        pos = lawRef.indexOf(dictKey, pos + 1);
      }

      // 额外支持承前省略“法”字的顿号写法，例如：“法第九十五条第一款、第九十条”
      if (!isMatched) {
        const numPart = dictKey.replace(/^法/, ''); // 如“第九十条”
        const regex = new RegExp(`[、,，\\s]${numPart}`);
        if (regex.test(lawRef)) {
          isMatched = true;
        }
      }
    } else {
      if (lawRef.includes(dictKey)) {
        isMatched = true;
      }
    }

    if (isMatched) {
      const articleItem = LAW_ARTICLES_DICT[dictKey];
      if (!matched.some(m => m.title === articleItem.title)) {
        matched.push(articleItem);
      }
    }
  });

  return matched;
}
